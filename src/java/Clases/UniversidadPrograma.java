package Clases;

/**
 *
 * @author leonardo
 */
public class UniversidadPrograma {
    private String nombreuni;
    private String nombreprograma;
    private String mesfechaini;
    private String aniofechaini;
    private String mesfechafin;
    private String aniofechafin;
    
    public UniversidadPrograma(){
        super();
    }

    public String getnombreuni() {
        return nombreuni;
    }

    public void setnombreuni(String nombreuni) {
        this.nombreuni = nombreuni;
    }
    
    public String getnombreprograma() {
        return nombreprograma;
    }

    public void setnombreprograma(String nombreprograma) {
        this.nombreprograma = nombreprograma;
    }
    
    public String getmesfechaini() {
        return mesfechaini;
    }

    public void setmesfechaini(String mesfechaini) {
        this.mesfechaini = mesfechaini;
    }
    
    public String getaniofechaini() {
        return aniofechaini;
    }

    public void setaniofechaini(String aniofechaini) {
        this.aniofechaini = aniofechaini;
    }
    
    public String getmesfechafin() {
        return mesfechafin;
    }

    public void setmesfechafin(String mesfechafin) {
        this.mesfechafin = mesfechafin;
    }
    
    public String getaniofechafin() {
        return aniofechafin;
    }

    public void setaniofechafin(String aniofechafin) {
        this.aniofechafin = aniofechafin;
    }
    
}
