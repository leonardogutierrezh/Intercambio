public class Materias {
    private String codigoExt;
    private String codigoUSB;
    private String denominacionExt;
    private String denominacionUSB;
    private String creditosUSB;
    private String creditosExt;
    
    public String getCodigoExt() {
        return codigoExt;
    }
    
    public void setCodigoExt(String codigoExt) {
        this.codigoExt = codigoExt;
    }

    public String getCodigoUSB() {
        return codigoUSB;
    }

    public void setCodigoUSB(String codigoUSB) {
        this.codigoUSB = codigoUSB;
    }
    
    public String getDenominacionExt() {
        return denominacionExt;
    }

    public void setDenominacionExt(String denominacionExt) {
        this.denominacionExt = denominacionExt;
    }
    
    public String getDenominacionUSB() {
        return denominacionUSB;
    }
    
    public void setDenominacionUSB(String denominacionUSB) {
        this.denominacionUSB = denominacionUSB;
    }
    
    public String getCreditosUSB() {
        return creditosUSB;
    }
    
    public void setCreditosUSB(String creditosUSB) {
        this.creditosUSB = creditosUSB;
    }
    
    public String getCreditosExt() {
        return creditosExt;
    }
    
    public void setCreditosExt(String creditosExt) {
        this.creditosExt = creditosExt;
    }
    
    
}
